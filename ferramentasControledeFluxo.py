# função if:
# A função if é usada para executar um bloco de código se uma condição for verdadeira.

# A sintaxe básica é a seguinte:
# if condição:
#     # bloco de código a ser executado se a condição for verdadeira
#else:
#     # bloco de código a ser executado se a condição for falsa
#
# Exemplo:
idade = 18
if idade >= 18:
    print("Você é maior de idade.")
else:
    print("Você é menor de idade.") 
    
    
# A saída será "Você é maior de idade." porque a condição idade >= 18 é verdadeira.

# Você pode usar operadores de comparação como ==, !=, <, >, <= e >= para comparar valores.

