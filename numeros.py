# Este é um arquivo Python que contém uma uma preve explicação de como usar os operadores matemáticos básicos.
# Os operadores matemáticos básicos em Python são:
# adição (+), subtração (-), multiplicação (*), divisão (/), módulo (%) e exponenciação (**).
# Abaixo estão exemplos de como usar cada um deles: 
# Adição (+)
soma = 5 + 3
print("A soma de 5 e 3 é:", soma) # a saída será em um número inteiro, pois não foi definida o tipo antes.

# Subtração (-)
subtracao = 10 - 4
print("A subtração de 10 e 4 é:", subtracao) # a saída será em um número inteiro, pois não foi definida o tipo antes.

# Multiplicação (*)
multiplicacao = 6 * 7   
print("A multiplicação de 6 e 7 é:", multiplicacao) # a saída será em um número inteiro, pois não foi definida o tipo antes.  

# Divisão (/)
divisao = 15 / 3
print("A divisão de 15 por 3 é:", divisao) # a saída será em um número com vírgula(ou float),
# pois no python uma divisão por padrão retorna um número com vírgula.
# Se você quiser um número inteiro, use a divisão inteira (//) ou a função int() para converter o resultado.
# Divisão inteira (//)  
divisao_inteira = 15 // 4
print("A divisão inteira de 15 por 4 é:", divisao_inteira) # a saída será em um número inteiro, pois não foi definida o tipo antes.     
# função int() para converter o resultado
divisao_inteira = int(15 / 4)
print("A divisão inteira de 15 por 4 é:", divisao_inteira) # a saída será em um número inteiro, pois foi definida o tipo antes.

# Módulo (%)
modulo = 10 % 3 
print("O módulo de 10 por 3 é:", modulo) # a saída será em um número inteiro, pois não foi definida o tipo antes.

# Exponenciação (**)
exponenciacao = 2 ** 3
print("2 elevado a 3 é:", exponenciacao) # a saída será em um número inteiro, pois não foi definida o tipo antes.
# O operador de exponenciação é usado para elevar um número a uma potência.
# Por exemplo, 2 elevado a 3 é igual a 8.
# Você pode usar esses operadores matemáticos em expressões mais complexas também.  
# Por exemplo:
expressao_complexa = (5 + 3) * (10 - 2) / 4
print("O resultado da expressão complexa é:", expressao_complexa) # a saída será em um número com vírgula(ou float),
# pois no python uma divisão por padrão retorna um número com vírgula.  
# Você pode usar parênteses para controlar a ordem das operações.
# As operações dentro dos parênteses serão realizadas primeiro.
# Se você quiser fazer operações com números negativos, você pode usar o sinal de menos (-) antes do número.
# Por exemplo:
numero_negativo = -5
print("O número negativo é:", numero_negativo) # a saída será um número inteiro negativo, pois não foi definida o tipo antes. 
# Você também pode usar os operadores matemáticos com números negativos.
# Por exemplo: 
soma_negativa = -5 + 3
print("A soma de -5 e 3 é:", soma_negativa) # a saída será um número inteiro negativo, pois não foi definida o tipo antes.
# Você pode usar os operadores matemáticos em variáveis também.
# Por exemplo:
a = 5
b = 3
soma = a + b
print("A soma de a e b é:", soma) # a saída será um número inteiro, pois não foi definida o tipo antes.

